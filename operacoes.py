def somarNumero(numero1,numero2):
    Soma=numero1+numero2
    return Soma


def subtrairNumero(numero1,numero2):
    Subtrair=numero1-numero2
    return Subtrair


def multiplicacaoNumero(numero1,numero2):
    multiplicacao=numero1*numero2
    return multiplicacao


def dividirNumero(numero1,numero2):
    dividir=numero1/numero2
    return dividir
